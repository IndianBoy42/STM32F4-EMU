#define JOYSTICK_DEADZONE 70
__forceinline int16_t joystick_conv(uint16_t j) {
  int16_t tmp = ((int16_t)j) - 2048;
  if (tmp < JOYSTICK_DEADZONE && tmp > -JOYSTICK_DEADZONE) return 0;
  else return tmp;
}
